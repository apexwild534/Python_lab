# Prompt the user to enter their name
name = input("Enter your name: ")

# Print a greeting message with the name in uppercase and lowercase
print(f"Hello, {name.upper()}!")
print(f"Hello, {name.lower()}!")

# Calculate and display the length of the entered name
name_length = len(name)
print(f"Your name has {name_length} characters.")

# Ask the user to enter a sentence
sentence = input("Enter a sentence: ")

# Display the entered sentence
print(f"You entered: {sentence}")
